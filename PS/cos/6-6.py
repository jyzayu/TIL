# 모든 한칸에 대해 이렇게 한칸 두칸 접었을 때 나오는 계산을 직접 작성했네 
def solution(grid):
    answer = 0
    for i in range(4):
        for j in range(4):
            for k in range(j + 1, 4, 2):
                # 0, 2, 3
                #max( 02 + 03, 20 + 30 )

                # 0 0 1,  0 0 3
                # max( 00 + 01, 01 + 10)  max( 00 + 03 , 03 + 30)
                answer = max(answer, max(grid[i][j] + grid[j][k], grid[i][j] + grid[k][i]))
    return answer

